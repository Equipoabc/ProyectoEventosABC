package Logica;

/**
 *
 * @author Luis
 */
public class Sede {
    
    	private String id_sede;
	private String nombre_sede;
	private String ciudad;
	private String calle;
	private String carrera;
	private String barrio;
	private String telefono;
	private String cedula_ge;

    /**
     * @return the id_sede
     */
    public String getId_sede() {
        return id_sede;
    }

    /**
     * @param id_sede the id_sede to set
     */
    public void setId_sede(String id_sede) {
        this.id_sede = id_sede;
    }

    /**
     * @return the nombre_sede
     */
    public String getNombre_sede() {
        return nombre_sede;
    }

    /**
     * @param nombre_sede the nombre_sede to set
     */
    public void setNombre_sede(String nombre_sede) {
        this.nombre_sede = nombre_sede;
    }

    /**
     * @return the ciudad
     */
    public String getCiudad() {
        return ciudad;
    }

    /**
     * @param ciudad the ciudad to set
     */
    public void setCiudad(String ciudad) {
        this.ciudad = ciudad;
    }

    /**
     * @return the calle
     */
    public String getCalle() {
        return calle;
    }

    /**
     * @param calle the calle to set
     */
    public void setCalle(String calle) {
        this.calle = calle;
    }

    /**
     * @return the carrera
     */
    public String getCarrera() {
        return carrera;
    }

    /**
     * @param carrera the carrera to set
     */
    public void setCarrera(String carrera) {
        this.carrera = carrera;
    }

    /**
     * @return the barrio
     */
    public String getBarrio() {
        return barrio;
    }

    /**
     * @param barrio the barrio to set
     */
    public void setBarrio(String barrio) {
        this.barrio = barrio;
    }

    /**
     * @return the telefono
     */
    public String getTelefono() {
        return telefono;
    }

    /**
     * @param telefono the telefono to set
     */
    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    /**
     * @return the cedula_ge
     */
    public String getCedula_ge() {
        return cedula_ge;
    }

    /**
     * @param cedula_ge the cedula_ge to set
     */
    public void setCedula_ge(String cedula_ge) {
        this.cedula_ge = cedula_ge;
    }
}
